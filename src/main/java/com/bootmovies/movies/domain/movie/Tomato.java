package com.bootmovies.movies.domain.movie;

public class Tomato {
    private int meter;
    private String image;
    private double rating;
    private int reviews;
    private int fresh;
    private String consensus;
    private int userMeter;
    private int userRating;
    private int userReviews;

    public int getMeter() {
        return meter;
    }

    public void setMeter(int meter) {
        this.meter = meter;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public double getRating() {
        return rating;
    }

    public void setRating(double rating) {
        this.rating = rating;
    }

    public int getReviews() {
        return reviews;
    }

    public void setReviews(int reviews) {
        this.reviews = reviews;
    }

    public int getFresh() {
        return fresh;
    }

    public void setFresh(int fresh) {
        this.fresh = fresh;
    }

    public String getConsensus() {
        return consensus;
    }

    public void setConsensus(String consensus) {
        this.consensus = consensus;
    }

    public int getUserMeter() {
        return userMeter;
    }

    public void setUserMeter(int userMeter) {
        this.userMeter = userMeter;
    }

    public int getUserRating() {
        return userRating;
    }

    public void setUserRating(int userRating) {
        this.userRating = userRating;
    }

    public int getUserReviews() {
        return userReviews;
    }

    public void setUserReviews(int userReviews) {
        this.userReviews = userReviews;
    }


}
